func smallModelNode(small SmallModelConfig) *yaml.Node {
	return yamlMapping(
		yamlEntry("model", yamlScalar(small.Model)),
		yamlEntry("max_tokens", optionalIntNode(small.MaxTokens)),
		yamlEntry("temperature", optionalFloatNode(small.Temperature)),
		yamlEntry("top_p", optionalFloatNode(small.TopP)),
		yamlEntry("top_k", optionalIntNode(small.TopK)),
		yamlEntry("presence_penalty", optionalFloatNode(small.PresencePenalty)),
		yamlEntry("extra_body", mapNode(small.ExtraBody)),
		yamlEntry("reasoning_effort", yamlScalar(small.ReasoningEffort)),
	)
}

func supportedModelsNode(models []ModelCapabilities) *yaml.Node {
	seq := &yaml.Node{Kind: yaml.SequenceNode}
	for _, model := range models {
		entries := []yamlNodeEntry{
			yamlEntry("model", yamlScalar(model.Model)),
			yamlEntry("compatible", yamlBool(model.Compatible)),
			yamlEntry("response", yamlBool(model.Response)),
			yamlEntry("tools", yamlBool(model.Tools)),
		}
		if model.JSONResponse != nil {
			entries = append(entries, yamlEntry("json_response", yamlBool(*model.JSONResponse)))
		}
		if model.JSONSchema != nil {
			entries = append(entries, yamlEntry("json_schema", yamlBool(*model.JSONSchema)))
		}
		entries = append(entries, yamlEntry("reasoning", yamlMapping(
			yamlEntry("traces", yamlBool(model.Reasoning.Traces)),
			yamlEntry("efforts", stringSliceNode(model.Reasoning.Efforts)),
		)))
		seq.Content = append(seq.Content, yamlMapping(entries...))
	}
	return seq
}

func stringSliceNode(values []string) *yaml.Node {
	seq := &yaml.Node{Kind: yaml.SequenceNode}
	for _, value := range values {
		seq.Content = append(seq.Content, yamlScalar(value))
	}
	return seq
}

func optionalIntNode(value *int) *yaml.Node {
	if value == nil {
		return yamlNull()
	}
	return yamlInt(*value)
}

func optionalFloatNode(value *float64) *yaml.Node {
	if value == nil {
		return yamlNull()
	}
	return yamlFloat(*value)
}

func mapNode(values map[string]any) *yaml.Node {
	node := yamlMapping()
	keys := make([]string, 0, len(values))
	for key := range values {
		keys = append(keys, key)
	}
	slices.Sort(keys)
	for _, key := range keys {
		node.Content = append(node.Content, yamlScalar(key), anyNode(values[key]))
	}
	return node
}

func anyNode(value any) *yaml.Node {
	node := &yaml.Node{}
	if err := node.Encode(value); err != nil {
		return yamlScalar(fmt.Sprint(value))
	}
	return node
}

func exampleProfile(profile Profile) Profile {
	profile = cloneProfile(profile)
	profile.APIKey = canonicalEnvRef(profile.APIKey)
	profile = applyProfileDefaults(profile)
	if profile.GitHubToken == "" {
		profile.GitHubToken = DefaultGitHubTokenRef
	} else {
		profile.GitHubToken = canonicalEnvRef(profile.GitHubToken)
	}
	if profile.GitLabToken == "" {
		profile.GitLabToken = DefaultGitLabTokenRef
	} else {
		profile.GitLabToken = canonicalEnvRef(profile.GitLabToken)
	}
	if profile.GitLabBaseURL == "" {
		profile.GitLabBaseURL = DefaultGitLabBaseURLRef
	} else {
		profile.GitLabBaseURL = canonicalEnvRef(profile.GitLabBaseURL)
	}
	return profile
}

func canonicalEnvRef(value string) string {
	matches := envReferencePattern.FindStringSubmatch(strings.TrimSpace(value))
	if len(matches) == 0 {
		return value
	}
	name := matches[1]
	if name == "" {
		name = matches[2]
	}
	return "${" + name + "}"
}

type yamlNodeEntry struct {
	key   string
	value *yaml.Node
}

func yamlEntry(key string, value *yaml.Node) yamlNodeEntry {
	return yamlNodeEntry{key: key, value: value}
}

func yamlMapping(entries ...yamlNodeEntry) *yaml.Node {
	node := &yaml.Node{
		Kind: yaml.MappingNode,
		Tag:  "!!map",
	}
	for _, entry := range entries {
		node.Content = append(node.Content, yamlScalar(entry.key), entry.value)
	}
	return node
}

func yamlScalar(value string) *yaml.Node {
	return &yaml.Node{
		Kind:  yaml.ScalarNode,
		Tag:   "!!str",
		Value: value,
	}
}

func yamlNull() *yaml.Node {
	return &yaml.Node{
		Kind:  yaml.ScalarNode,
		Tag:   "!!null",
		Value: "",
	}
}

func yamlInt(value int) *yaml.Node {
	return &yaml.Node{
		Kind:  yaml.ScalarNode,
		Tag:   "!!int",
		Value: fmt.Sprintf("%d", value),
	}
}

func yamlFloat(value float64) *yaml.Node {
	return &yaml.Node{
		Kind:  yaml.ScalarNode,
		Tag:   "!!float",
		Value: fmt.Sprintf("%g", value),
	}
}

func yamlBool(value bool) *yaml.Node {
	return &yaml.Node{
		Kind:  yaml.ScalarNode,
		Tag:   "!!bool",
		Value: fmt.Sprintf("%t", value),
	}
}

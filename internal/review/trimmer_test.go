func TestTrimmerHeadroomReducesBudget(t *testing.T) {
	build := func() *model.ReviewContext {
		return &model.ReviewContext{
			DiffFiles: []model.DiffFile{
				testDiffFile("a.go", 20),
				testDiffFile("b.go", 20),
			},
		}
	}
	total := len(testDiffFile("a.go", 20).Content) * 2

	without, err := NewTrimmer(total, exactEstimator{}).Trim(build())
	if err != nil {
		t.Fatalf("Trim: %v", err)
	}
	if len(without.DiffFiles) != 2 {
		t.Fatalf("without headroom diff files = %#v, want untouched", without.DiffFiles)
	}

	with, err := NewTrimmer(total, exactEstimator{}, WithHeadroomTokens(total/2)).Trim(build())
	if err != nil {
		t.Fatalf("Trim: %v", err)
	}
	if len(with.DiffFiles) != 1 {
		t.Fatalf("with headroom diff files = %#v, want one file evicted", with.DiffFiles)
	}
}

